//Створіть програму, в якій ви оголосите змінні для кожного з примітивних типів даних: рядок, число, буль, null та undefined.
// Присвойте їм значення та виведіть кожну змінну на консоль
// Змінні для примітивних типів даних
let myString = "Це рядок";      // Рядок (string)
let myNumber = 35;              // Число (number)
let myBoolean = true;           // Логічне значення (boolean)
let myNull = null;              // Null
let myUndefined;                // Undefined

// Вивід кожної змінної на консоль
console.log("Рядок (string):", myString);
console.log("Число (number):", myNumber);
console.log("Логічне значення (boolean):", myBoolean);
console.log("Null:", myNull);
console.log("Undefined:", myUndefined);

